package ocp;

public class MotoEsportiva extends Moto {

	private boolean flexCombustivel;
	private String tipoCarenagem;
	
	public boolean isFlexCombustivel() {
		return flexCombustivel;
	}
	public void setFlexCombustivel(boolean flexCombustivel) {
		this.flexCombustivel = flexCombustivel;
	}
	public String getTipoCarenagem() {
		return tipoCarenagem;
	}
	public void setTipoCarenagem(String tipoCarenagem) {
		this.tipoCarenagem = tipoCarenagem;
	}
	
	
	
}
